-- Auto-set status timestamps so the frontend only needs to set status
create or replace function public.set_inquiry_status_timestamps()
returns trigger
language plpgsql
as $$
begin
  if new.status is distinct from old.status then
    if new.status = 'notified'  and new.notified_at  is null then new.notified_at  := now(); end if;
    if new.status = 'fulfilled' and new.fulfilled_at is null then new.fulfilled_at := now(); end if;
    if new.status = 'cancelled' and new.cancelled_at is null then new.cancelled_at := now(); end if;
  end if;
  return new;
end;
$$;

create trigger inquiries_set_status_timestamps
before update of status on public.inquiries
for each row execute function public.set_inquiry_status_timestamps();

