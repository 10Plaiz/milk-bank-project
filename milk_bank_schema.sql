-- Keep batch.total_volume_ml in sync with batch_collections rows
create or replace function public.refresh_batch_total_volume()
returns trigger
language plpgsql
as $$
declare
  target uuid;
begin
  target := case tg_op when 'DELETE' then old.batch_id else new.batch_id end;

  update public.batches
  set    total_volume_ml = coalesce(
    (select sum(volume_ml) from public.batch_collections where batch_id = target), 0
  )
  where  id = target;

  if tg_op = 'UPDATE' and old.batch_id is distinct from new.batch_id then
    update public.batches
    set    total_volume_ml = coalesce(
      (select sum(volume_ml) from public.batch_collections where batch_id = old.batch_id), 0
    )
    where  id = old.batch_id;
  end if;

  return case tg_op when 'DELETE' then old else new end;
end;
$$;

create trigger batch_collections_refresh_batch_total
after insert or update or delete on public.batch_collections
for each row execute function public.refresh_batch_total_volume();

