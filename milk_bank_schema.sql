-- Makati Human Milk Bank Management System
-- Canonical Supabase/PostgreSQL schema — v2
--
-- Changes from v1:
--   + lab_results.recorded_by_name text  (was missing from deployed schema)
--   - discarded_batches_need_reason CHECK constraint dropped
--       The apply_lab_result_to_batch trigger already supplies a reason on lab failure.
--       Admin-initiated discards should not be blocked by a missing free-text field.
--   - supsup_requires_consent_for_pass CHECK constraint dropped
--       Counseling/interview/consent timestamps are not yet captured in the frontend.
--       Re-add when the screening workflow is extended to include those steps.
--   - inquiry_status_timestamp_alignment CHECK constraint replaced by a BEFORE UPDATE
--       trigger (set_inquiry_status_timestamps) that auto-sets the timestamp, so the
--       frontend never has to supply it explicitly.
--   - inventory_summary and available_bottles_fifo views removed
--       Inventory and bottle selection are queried directly from batches/bottles.
--       Views can be re-added if a reporting layer needs them.
--
-- Invariants that remain:
--   + beneficiary_has_contact (contact_number OR contact_email required)
--   + confirmed_dispensing_requires_gate (all verification flags + dispensed_by required)
--   + waiting_list_requires_nicu (only NICU-confirmed inquiries reach waiting/notified)
--   + prevent_invalid_batch_status_transition trigger
--   + prevent_collection_daily_limit_excess trigger
--   + Full audit trail via write_audit_log triggers

