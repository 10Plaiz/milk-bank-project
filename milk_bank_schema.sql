-- Enforce valid status transitions
create or replace function public.prevent_invalid_batch_status_transition()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' or new.status = old.status then
    return new;
  end if;

  case old.status
    when 'raw'            then if new.status in ('pre_testing', 'discarded')    then return new; end if;
    when 'pre_testing'    then if new.status in ('pre_test_passed', 'discarded') then return new; end if;
    when 'pre_test_passed' then if new.status in ('pasteurized', 'discarded')   then return new; end if;
    when 'pasteurized'    then if new.status in ('post_testing', 'discarded')   then return new; end if;
    when 'post_testing'   then if new.status in ('ready', 'discarded')          then return new; end if;
    when 'ready'          then if new.status in ('dispensed', 'discarded')      then return new; end if;
    when 'dispensed'      then raise exception 'batch status dispensed is terminal';
    when 'discarded'      then raise exception 'batch status discarded is terminal';
    else null;
  end case;

  raise exception 'invalid batch status transition from % to %', old.status, new.status;
end;
$$;

create trigger batches_prevent_invalid_status_transition
before update of status on public.batches
for each row execute function public.prevent_invalid_batch_status_transition();

