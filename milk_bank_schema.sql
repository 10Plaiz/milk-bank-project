create table public.audit_logs (
  id          uuid        primary key default gen_random_uuid(),
  table_name  text        not null,
  record_id   uuid,
  action      text        not null check (action in ('insert', 'update', 'delete')),
  changed_by  uuid        references public.profiles(id) on delete set null,
  changed_at  timestamptz not null default now(),
  old_row     jsonb,
  new_row     jsonb
);

create index audit_logs_table_record_idx on public.audit_logs(table_name, record_id);
create index audit_logs_changed_at_idx   on public.audit_logs(changed_at);

create or replace function public.write_audit_log()
returns trigger
language plpgsql security definer
set search_path = public
as $$
declare
  affected_id  uuid;
  old_payload  jsonb;
  new_payload  jsonb;
begin
  if tg_op = 'INSERT' then
    affected_id := new.id; old_payload := null;        new_payload := to_jsonb(new);
  elsif tg_op = 'UPDATE' then
    affected_id := new.id; old_payload := to_jsonb(old); new_payload := to_jsonb(new);
  elsif tg_op = 'DELETE' then
    affected_id := old.id; old_payload := to_jsonb(old); new_payload := null;
  end if;

  insert into public.audit_logs (table_name, record_id, action, changed_by, old_row, new_row)
  values (tg_table_name, affected_id, lower(tg_op), auth.uid(), old_payload, new_payload);

  return case tg_op when 'DELETE' then old else new end;
end;
$$;

